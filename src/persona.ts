export function getJudgment(issues: string[]): string {
    if (issues.length === 0) {
        return '哦？一个错误都没有？嘿嘿~看来今天不需要本堂主的"送葬服务"嘛！可以的！';
    }
    const opening = pick(JUDGMENT_OPENINGS);
    const verdict = pick(JUDGMENT_VERDICTS);
    const list = issues.map((i) => `  🦋 ${i}`).join('\n');
    return `${opening}\n\n${list}\n\n${verdict}`;
}

